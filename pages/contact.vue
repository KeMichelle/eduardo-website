    },
  ],
});

// Form data
const form = ref<ContactForm>({
  firstName: '',
  lastName: '',
  email: '',
  phone: '',
  company: '',
  country: '',
  subject: '',
  message: '',
  newsletter: false,
});

const errors = ref<FormErrors>({});
const isSubmitting = ref(false);
const showSuccessToast = ref(false);

// Countries list (simplified)
const countries = [
  'United States',
  'Canada',
  'United Kingdom',
  'Germany',
  'France',
  'Spain',
  'Italy',
  'Netherlands',
  'Sweden',
  'Norway',
  'Denmark',
  'Australia',
  'New Zealand',
  'Japan',
  'South Korea',
  'Singapore',
  'Ecuador',
  'Brazil',
  'Argentina',
  'Colombia',
  'Mexico',
  'Chile',
];

// Form validation
const validateForm = (): boolean => {
  errors.value = {};

  if (!form.value.firstName.trim()) {
    errors.value.firstName = 'First name is required';
  }

  if (!form.value.lastName.trim()) {
    errors.value.lastName = 'Last name is required';
  }

  if (!form.value.email.trim()) {
    errors.value.email = 'Email is required';
  } else if (!/\S+@\S+\.\S+/.test(form.value.email)) {
    errors.value.email = 'Please enter a valid email address';
  }

  if (!form.value.subject) {
    errors.value.subject = 'Please select a subject';
  }

  if (!form.value.message.trim()) {
    errors.value.message = 'Message is required';
  } else if (form.value.message.trim().length < 10) {
    errors.value.message = 'Message must be at least 10 characters long';
  }

  return Object.keys(errors.value).length === 0;
};

// EmailJS configuration from environment variables
const config = useRuntimeConfig();
const EMAILJS_SERVICE_ID = config.public.emailjsServiceId;
const EMAILJS_TEMPLATE_ID = config.public.emailjsTemplateId;
const EMAILJS_PUBLIC_KEY = config.public.emailjsPublicKey;
const RECAPTCHA_SITE_KEY = config.public.recaptchaSiteKey;

// reCAPTCHA v3 token generation
const getRecaptchaToken = async (): Promise<string> => {
  return new Promise((resolve, reject) => {
    try {
      // @ts-ignore - grecaptcha loaded via CDN
      if (typeof grecaptcha === 'undefined') {
        reject(new Error('reCAPTCHA not loaded'));
        return;
      }

      // @ts-ignore
      grecaptcha.ready(() => {
        // @ts-ignore
        grecaptcha
          .execute(RECAPTCHA_SITE_KEY, { action: 'submit' })
          .then((token: string) => {
            resolve(token);
          })
          .catch((error: any) => {
            reject(error);
          });
      });
    } catch (error) {
      reject(error);
    }
  });
};

// Form submission
const submitForm = async () => {
  if (!validateForm()) {
    return;
  }

  isSubmitting.value = true;

  try {
    // Get reCAPTCHA token
    let recaptchaToken = '';
    if (RECAPTCHA_SITE_KEY) {
      try {
        recaptchaToken = await getRecaptchaToken();
      } catch (error) {
        console.warn('reCAPTCHA failed, continuing without it:', error);
      }
    }

    // Send email using EmailJS
    const templateParams = {
      from_name: `${form.value.firstName} ${form.value.lastName}`,
      from_email: form.value.email,
      phone: form.value.phone || 'Not provided',
      company: form.value.company || 'Not provided',
      country: form.value.country || 'Not provided',
      subject: form.value.subject,
      message: form.value.message,
      newsletter: form.value.newsletter ? 'Yes' : 'No',
      to_email: 'eduardo.p.gflex@outlook.com',
      'g-recaptcha-response': recaptchaToken,
    };

    // @ts-ignore - EmailJS loaded via CDN
    await window.emailjs.send(
      EMAILJS_SERVICE_ID,
      EMAILJS_TEMPLATE_ID,
      templateParams,
      EMAILJS_PUBLIC_KEY
    );

    // Reset form
    form.value = {
      firstName: '',
      lastName: '',
      email: '',
      phone: '',
      company: '',
      country: '',
      subject: '',
      message: '',
      newsletter: false,
    };

    // Show success message
    showSuccessToast.value = true;

    // Auto hide toast after 5 seconds
    setTimeout(() => {
      showSuccessToast.value = false;
    }, 5000);
  } catch (error) {
    console.error('Form submission error:', error);
    alert(
      'Failed to send message. Please try again or email directly at eduardo.p.gflex@outlook.com'
    );
  } finally {
    isSubmitting.value = false;
  }
};
