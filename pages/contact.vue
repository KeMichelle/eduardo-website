<script setup lang="ts">
interface ContactForm {
  firstName: string;
  lastName: string;
  email: string;
  phone: string;
  company: string;
  country: string;
  subject: string;
  message: string;
  newsletter: boolean;
}

interface FormErrors {
  firstName?: string;
  lastName?: string;
  email?: string;
  subject?: string;
  message?: string;
}

// Meta data
useHead({
  title: 'Contact Eduardo Penafiel - EDUP GlobalFlex | Get In Touch',
  meta: [
    {
      name: 'description',
      content:
